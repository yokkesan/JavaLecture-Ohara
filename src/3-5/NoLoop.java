public class NoLoop {
    public static void main(String[] args) {

        int i = 1;
        System.out.println("i = " + i);
        i++;

        System.out.println("i = " + i);
        i++;

        System.out.println("i = " + i);
        i++;

        System.out.println("i = " + i);
        i++;

        System.out.println("i = " + i);
        i++;

        System.out.println("i = " + i);
        i++;

        System.out.println("i = " + i);
        i++;

        System.out.println("i = " + i);
        i++;

        System.out.println("i = " + i);
        i++;

        System.out.println("i = " + i);
        i++;

        System.out.println("ループ終了");
    }
}